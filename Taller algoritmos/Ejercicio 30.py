n1 = float(input('Introduzca el primer número: '))
n2 = float(input('Introduzca el segundo número: '))
n3 = float(input('Introduzca el tercer número: '))
if n1 + n2 > n3:
    print('La suma de los dos primeros números es mayor a 3')
elif n1 + n2 == n3:
    print('La suma de los dos primeros números es igual a 3')
else:
    print('La suma de los dos primeros números es menor a 3')
